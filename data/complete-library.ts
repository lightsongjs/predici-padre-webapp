export const searchIndex = [
  ...liturgicalSermons,
  ...coursesSermons,
  ...saintsSermons,
  ...topicalSermons,
  ...archivedSermons
];

export function searchLibrary(query: string): LibraryItem[] {
  const q = query.toLowerCase();
  return searchIndex.filter(item => {
    const titleMatch = item.title.toLowerCase().includes(q);
    const categoryMatch = item.category.toLowerCase().includes(q);
    const keywordMatch = item.keywords?.some(k => k.toLowerCase().includes(q));
    return titleMatch || categoryMatch || keywordMatch;
  });
}

export function getSermonsByCategory(category: string): LibraryItem[] {
  switch(category.toLowerCase()) {
    case 'liturgical': return liturgicalSermons;
    case 'courses': return coursesSermons;
    case 'saints': return saintsSermons;
    case 'topical': return topicalSermons;
    case 'archived': return archivedSermons;
    default: return [];
  }
}
